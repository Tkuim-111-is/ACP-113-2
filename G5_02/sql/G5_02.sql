-- Create the AUTHORISBN table
CREATE TABLE AUTHORISBN
(
    AUTHORID INT NOT NULL,

    ISBN VARCHAR(13) NOT NULL,

    FOREIGN KEY (AUTHORID) REFERENCES AUTHORS(AUTHORID),

    FOREIGN KEY (ISBN) REFERENCES TITLES(ISBN)

