-- Create the TITLES table
Create TABLE TITLES
(
    ISBN VARCHAR(13) NOT NULL,

    TITLE VARCHAR(255) NOT NULL,

    EDITIONNUMBER INT NOT NULL,

    COPYRIGHT VARCHAR(4) NOT NULL,

    PRIMARY KEY (ISBN)
);

